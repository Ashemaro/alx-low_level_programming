#include "main.h"
/**
 * print_rev - reverses a string then a new line
 * @s: string to be reversed
 * return: returns nothing
 */

void print_rev(char *s)
{
	int longi = 0;
	int o;

	while (*s != '\0')
	{
		longi++;
		s++;
	}
	s--;
	for (o = longi; o > 0; o--)
	{
		_putchar(*s);
		s--;
	}
	_putchar('\n');
}
